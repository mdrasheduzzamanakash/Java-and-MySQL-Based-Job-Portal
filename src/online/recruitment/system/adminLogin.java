    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        Connection connection;
        PreparedStatement ps;
        
        try {
            connection = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/online_rec_system?autoReconnect=true&useSSL=false","root","password");
            ps = (PreparedStatement) connection.prepareStatement("SELECT * FROM admin WHERE user_name = ? AND pass = ?");
            ps.setString(1, lu.getText());
            ps.setString(2, String.valueOf(lp.getPassword()));
            ResultSet rs = ps.executeQuery();
            
            if(rs.next())
            {
                setVisible(false);
                a_admin_job_seeker ad = new a_admin_job_seeker();
                ad.setVisible(true);
                
            }
            else {
                JOptionPane.showMessageDialog(this, "invalid User" );
            }
        } catch (SQLException ex) {
            Logger.getLogger(company.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loginActionPerformed

